package com.Mjava;

public class TypeCastingDemo {

	public static void main(String[] args) {
		 int i=10;
		 long j=i;
		 double d=j;
		 float g=j;
		 
		 System.out.println("Integer value : "+i);
		 System.out.println("Long  value : "+j);
		 System.out.println("Double  value : "+d);
		 System.out.println("Float  value : "+g);

	}

}
